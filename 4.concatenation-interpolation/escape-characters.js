const newline = "Line1\nLine2"; // \n - New line
const tab = "Column1\tColumn2"; // \t - Tab
const backslash = "Path\\to\\file"; // \\ - Backslash
const doubleQuote = "He said \"Hello\""; // \" - Double quote
const singleQuote = 'It\'s a sunny day'; // \' - Single quote

console.log(newline); // "Line1
console.log(tab); // "Column1    Column2"
console.log(backslash); // "Path\to\file"
console.log(doubleQuote); // "He said "Hello""
console.log(singleQuote); // "It's a sunny day"

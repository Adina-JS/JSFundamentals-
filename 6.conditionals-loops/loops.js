// ## `for` Loop
for (initialization; condition; increment) { 
    // code to be executed 
}
// Example
for (let i = 0; i < 5; i++) { 
    console.log("The number is " + i); 
}


// ## `while` Loop
while (condition) { 
    // code to be executed 
}
// Example
let i = 0;
while (i < 5) {
  console.log("The number is " + i);
  i++;
}
  


// ## Ternary Operator
// condition ? expressionIfTrue : expressionIfFalse
// Example
let age = 18; 
let canVote = age >= 18 ? "Yes" : "No"; 
console.log(canVote); // Output: Yes

// ## Multiple Inline Conditions
// condition ? expressionIfTrue : expressionIfFalse
// Example
let score = 85;
let grade = score >= 90 ? "A" :
 score >= 80 ? "B" :
 score >= 70 ? "C" : "D";
console.log(grade); // Output: B
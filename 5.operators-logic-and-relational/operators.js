// AND ( && )
true && true // returns true
true && false // retuns false

// OR ( || )
true || false // returns true
false || false // returns false

// NOT ( ! )
!true // returns false
!false // returns true

// Greater Than ( > )
5 > 3 // returns true

// Less Than ( < )
3 < 5 // returns true

// Greater Than or Equal to
5 >= 5 // returns true

// Less Than or Equal to
5 <= 5 // returns true

// Equality
5 == '5' // returns true

// Strict Equality
5 === '5' // returns false

// Inequality
5 != '5' // returns false

// Strict Inequality
5 !== '5' // returns true